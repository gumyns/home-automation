package org.openhab.binding.gumyns.internal.model.config;

public class ChannelJson {

    private String name, id, type, description;

    ChannelJson() {

    }

    public String getName() {
        return name;
    }

    public String getId() {
        return id;
    }

    public String getDescription() {
        return description;
    }

    public String getType() {
        return type;
    }
}
